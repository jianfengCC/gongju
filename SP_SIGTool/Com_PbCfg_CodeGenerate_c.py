def write_const_date(write_f):
    const_txt = ''
    sig_type = ''
    sig_id_list = parser_excel.get_sig_id_list()
    sig_init_list = parser_excel.get_sig_init_val_list()
    for signame in sig_id_list:
        for msg in parser_dbc.get_can_msg():
            msg_num = str(hex(msg.frame_id).upper()).split('X')[1]
            while(len(msg_num) < 3):
                msg_num = '0' + msg_num
            msg_id = '0X'+ msg_num
            # print(msg_id)
            for sig in msg.signals:
                if (sig.name + msg_id ) == signame:
                    sig_name = sig.name
                    sig_type = parser_dbc.get_date_type(parser_dbc.get_sig_type(sig.name))
                    init_val = '0x0'
                    if sig_init_list[signame] != '':
                        init_val = sig_init_list[signame]
                    const_txt = const_txt + \
'const   '+sig_type+'    Com_SignalInitValue_' + sig_name + '_0x'+ msg_num +'_INFO_Message = '+init_val+';\n'
    write_f.write(const_txt + '\n\n')

