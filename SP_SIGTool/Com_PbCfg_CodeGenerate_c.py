def write_ComSignal(write_f):
    sp_data = parser_txt.get_txt()
    sig_send_type_map = parser_excel.get_sig_send_type_map()
    sig_name_list = parser_excel.get_sig_name_list()
    sig_id_list = parser_excel.get_sig_id_list()
    temp_txt = 'const ComSignal_type ComSignal[] =   {\n'
    for signame in sig_id_list:
        # print('signame:',signame)
        msg_num = ''
        sig_name = ''
        cyc_time = 0
        sig_base_timer = ''
        sig_pop = ''
        sig_len = 0
        sig_start = 0
        sp_flag = 'FALSE'
        for msg in parser_dbc.get_can_msg():
            msg_num = str(hex(msg.frame_id).upper()).split('X')[1]
            while(len(msg_num) < 3):
                msg_num = '0' + msg_num
            msg_id = '0X'+ msg_num
            # print(msg_id)
            for sig in msg.signals:
                if (sig.name + msg_id ) == signame:
                    sig_name = sig.name
                    cyc_time = msg.cycle_time
                    if cyc_time == None or cyc_time == 0:
                        cyc_time = 50
                    elif cyc_time >= 500:
                        cyc_time = 500

                    sig_base_timer = 'CAN_BASE_RX_TIMER_MS_FACTOR'
                    if(parser_dbc.Is_Msg_Send(msg.name)):
                        sig_base_timer = 'CAN_BASE_TX_TIMER_MS_FACTOR'
                    sig_pop = sig_send_type_map[signame]

                    sig_len = sig.length
                    sig_start = sig.start

                    if sig.name.upper() in sp_data:
                        sp_flag = 'TRUE'

                    temp_txt = temp_txt + \
'    { // 0x'+ msg_num + '\n\
        .ComHandleId                = COMCONF_COMSIGNAL_'+sig_name.upper()+'_0X'+msg_num+'_INFO,\n\
        .ComIPduHandleId            = ComConf_ComIPdu_0x'+msg_num+'_INFO,\n\
        .ComFirstTimeoutFactor      = ('+ str(cyc_time * 10) +'/'+sig_base_timer+'),\n\
        .ComNotification            = COM_SIGNAL_CALLOUT,\n\
        .ComTimeoutFactor           = ('+ str(cyc_time * 10) +'/'+sig_base_timer+'),\n\
        .ComTimeoutNotification     = COM_SIGNAL_CALLOUT,\n\
        .ComErrorNotification       = COM_NO_FUNCTION_CALLOUT,\n\
        .ComTransferProperty        = '+ sig_pop +',\n\
        .ComUpdateBitPosition       = 0,\n\
        .ComSignalAsUseUpdateBit    = '+ sp_flag +',\n\
        .ComSignalInitValue         = &Com_SignalInitValue_' + sig_name + '_0x'+ msg_num +'_INFO_Message,\n\
        .ComBitPosition             = '+str(sig_start)+',\n\
        .ComBitSize                 = '+str(sig_len)+',\n\
        .ComSignalEndianess         = COM_LITTLE_ENDIAN,\n\
        .ComSignalType              = '+parser_dbc.get_sig_type(sig_name)+',\n\
        .Com_As_IsSignalGroup       = FALSE,\n\
        .ComGroupSignal             = NULL,\n\
        .ComRxDataTimeoutAction     = COM_TIMEOUT_DATA_ACTION_NONE,\n\
        .ComSigGwRoutingReq         = FALSE,\n\
        .ComOsekNmNetId             = COM_OSEKNM_INVALID_NET_ID,\n\
        .ComOsekNmNodeId            = 0,\n\
        .Com_As_EOL = 0\n\
    },\n'
    temp_txt = temp_txt + '''    {
        .Com_As_EOL                = 1
    }
};\n'''
    write_f.write(temp_txt)

